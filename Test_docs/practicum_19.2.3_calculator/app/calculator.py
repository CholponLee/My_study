class Calculator:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def multiply(self):
        return self.x * self.y

    def division(self):
        return self.x / self.y

    def subtraction(self):
        return self.x - self.y

    def adding(self):
        return self.x + self.y
